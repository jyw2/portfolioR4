class ProjectModel {
    constructor(
        name,
        subTitle,
        image,
        description,
        keywords,
        codeLink,
        viewLink
    ) {
        this.name = name
        this.subTitle = subTitle
        this.image = image
        this.description = description
        this.keywords = keywords
        this.codeLink = codeLink
        this.viewLink = viewLink
    }
}

export default ProjectModel